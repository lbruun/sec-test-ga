#!/bin/bash


printf "CREATING KIND CLUSTER\n-----------------------------------------------------------\n"
cat <<EOF | kind create cluster --config=-
kind: Cluster
apiVersion: kind.x-k8s.io/v1alpha4
nodes:
- role: control-plane
  kubeadmConfigPatches:
  - |
    kind: InitConfiguration
    nodeRegistration:
      kubeletExtraArgs:
        node-labels: "ingress-ready=true"
  extraPortMappings:
  - containerPort: 80
    hostPort: 80
    protocol: TCP
EOF

printf "\nKUBECTL GET ALL\n-----------------------------------------------------------\n"
kubectl get all
